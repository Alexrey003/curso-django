'''DJANGO

framework == area de trabajo
Los frameworks son ayudas para los desarroladores para agilizar la manera de creacion de proyectos y reducir su tiempo de creacion
Los frameworks ofrecen una estrcutura base para la elaboracion de un proyecto con objetivos especificos (es como una plantilla de trabajo)

Sirve para la creacion de apliaciones web agilizando su creacion de manera eficiente y rapida
- Django sirve para crear sitios web (complejos) agilizando su proceso de creacion
- Sirve para facilitar tareas repetitivas, pesadas y comunes
Una ventaja que tiene Django es que podemos reutilizar codigo de un sitio web a otro (Reutilizacion)

Patron Django
Toma como base el modelo llamado: Modelo Vista Controlador (MVC) divide en tres grandes modulos una aplicacion.

- Modelo: Gestion de BD
- Controlador: Comunicacion entre la vista y el modelo
- Vista: Muestra la informacion al usuario que ve e interactua.


Ventajas de dividir las aplicaciones en estos tres modulos:
- Apps mas funcionales
- Apps mas mantenibles
- Apps mas escalables

Django no esa exactamente este patron, pero sigue la misma filosofia.

El patron usa Django es:
Model Template View (MTV)

- Template (MTV) == Vista (MVC)
- View (MTV) == Controlador (MVC)
- Model (MTV) == Modelo (MVC)'''
